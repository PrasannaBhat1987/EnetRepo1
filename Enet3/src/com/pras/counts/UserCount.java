package com.pras.counts;

public class UserCount {
	
	private int admins;
	private int managers;
	private int reps;
	public int getAdmins() {
		return admins;
	}
	public void setAdmins(int admins) {
		this.admins = admins;
	}
	public int getManagers() {
		return managers;
	}
	public void setManagers(int managers) {
		this.managers = managers;
	}
	public int getReps() {
		return reps;
	}
	public void setReps(int reps) {
		this.reps = reps;
	}

}
